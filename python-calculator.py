# Advanced Calculator Build that will Add, Subtract, Multpily, Divide
# The user will be able to specify which operation they'd like to use

num1 = float(input("Enter first number: "))
op = input("Enter operator: ")
num2 = float(input("Enter second number: "))

if op == "+":
    print(num1 + num2)
elif op == "-":
    print(num1 - num2)
elif op == "/":
    print(num1 / num2)
elif op == "*":
    print(num1 * num2)
else:
    print("incorrect operator")
    
